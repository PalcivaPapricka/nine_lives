import pygame
from settings import TILE_SIZE

class World():
    def __init__(self, data):
        self.tile_list = []

        dirt_img = pygame.image.load('img/dirt.png')
        grass_img = pygame.image.load('img/grass.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(dirt_img, (TILE_SIZE, TILE_SIZE))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * TILE_SIZE
                    img_rect.y = row_count * TILE_SIZE
                    tile = (img, img_rect)
                    self.tile_list.append(tile)

                if tile == 2:
                    img = pygame.transform.scale(grass_img, (TILE_SIZE, TILE_SIZE))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * TILE_SIZE
                    img_rect.y = row_count * TILE_SIZE
                    tile = (img, img_rect)
                    self.tile_list.append(tile)

                if tile == 3:
                    water = Water(col_count * TILE_SIZE, row_count * TILE_SIZE + (TILE_SIZE // 2))
                    water_group.add(water)

                if tile == 4:
                    rat = Rat(col_count * TILE_SIZE, row_count * TILE_SIZE + (TILE_SIZE // 2))
                    rat_group.add(rat)

                if tile == 5:
                    spike = Spike(col_count * TILE_SIZE, row_count * TILE_SIZE + (TILE_SIZE // 2))
                    spike_group.add(spike)

                col_count += 1
            row_count += 1

    def draw(self , screen):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])


class Water(pygame.sprite.Sprite):
    def __init__(self, x, y):
        pygame.sprite.Sprite.__init__(self)
        img = pygame.image.load('img/water.png')
        self.image = pygame.transform.scale(img, (TILE_SIZE, TILE_SIZE // 2))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y

